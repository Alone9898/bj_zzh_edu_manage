<template>
  <div class="withdrawal">
    <div v-if="!details">
    <el-form :inline="true" :model="searchForm" class="demo-form-inline" label-width="100px">
      <el-form-item label="医生姓名" label-width="70px">
        <el-input clearable  @input="search" v-model="searchForm.doctor_name" placeholder="请输入医生姓名"></el-input>
      </el-form-item>
      <el-form-item label="医生手机" label-width="70px">
        <el-input clearable @input="search" v-model="searchForm.doctor_phone" placeholder="请输入医生手机"></el-input>
      </el-form-item>
      <el-form-item label="结算账期">
        <el-date-picker
            v-model="searchForm.accountTime"
            type="daterange"
            align="right"
            unlink-panels
            @input="search"
            value-format="yyyy-MM-dd"
            range-separator="至"
            start-placeholder="开始日期"
            end-placeholder="结束日期"
            :picker-options="pickerOptions">
        </el-date-picker>
      </el-form-item>
    </el-form>
    <div style="margin-bottom: 20px">
      <el-button type="primary" @click="search">查询<i class="el-icon-search el-icon--right"></i></el-button>
      <el-button type="info" @click="clear">重置<i class="el-icon-refresh-right el-icon--right"></i></el-button>
      <el-button plain @click="derive">导出<i class="el-icon-download el-icon--right"></i></el-button>
      <span class="decBox">
        <span>医生人数共 <i class="text-Danger"> {{ doctorDec_list.doctor_count }}人 </i> </span>
        <span>税前总计: <i class="text-Danger">{{ doctorDec_list.before_tax_count }}元 </i></span>
        <span>扣缴税额: <i class="text-Danger">{{ doctorDec_list.tax_total_money }}元 </i></span>
        <span>税后总计 : <i class="text-Danger">{{ doctorDec_list.after_tax_count }}元 </i></span>
      </span>
    </div>
    <div style="border: 1px solid #c9cdd6">
      <el-table
          :header-cell-style="{background:'#F5F5F5'}"
          :data="tableData"
          max-height="600"
          @sort-change="formatter"
          style="width: 100%"
      >
        <el-table-column
            prop="doctor_name"
            label="医生姓名"
            sortable="custom">
        </el-table-column>
        <el-table-column
            prop="phone"
            sortable="custom"
            align="center"
            label="手机号">
        </el-table-column>
        <el-table-column
            prop="before_tax_income"
            sortable="custom"
            align="center"
            min-width="100"
            label="税前总收入">
        </el-table-column>
        <el-table-column
            prop="tax_money"
            sortable="custom"
            align="center"
            label="扣缴税额">
        </el-table-column>
        <el-table-column
            prop="after_tax_income"
            sortable="custom"
            min-width="100"
            align="center"
            label="税后总收入">
        </el-table-column>
        <el-table-column
            prop="ready_settle_count"
            sortable="custom"
            min-width="140"
            align="center"
            label="预结算金额（未入账）">
        </el-table-column>
        <el-table-column
            prop="balance_money"
            sortable="custom"
            align="center"
            label="账户余额">
        </el-table-column>
        <el-table-column
            prop="cashout_money"
            sortable="custom"
            align="center"
            label="累计提现">
        </el-table-column>
        <el-table-column
            prop="address"
            fixed="right"
            align="center"
            label="操作">
          <template slot-scope="scope">
            <el-button @click="handleClick(scope.row)" type="text" size="small">详情</el-button>
          </template>
        </el-table-column>
      </el-table>
    </div>
    <!-- 分页 -->
    <div style="margin-top: 20px">
      <Pagination :total='total' :currentPage='currPage' :pageSize='pageSize' @handleSizeChange='handleSizeChange'
                  @handleCurrentChange='handleCurrentChange'></Pagination>
    </div>
    </div>
    <div v-if="details">
      <withdrawalTep @getBack="getBack" :doctorRow="doctorRow"></withdrawalTep>
    </div>
  </div>
</template>

<script>
import Pagination from '@/components/Pagination.vue';
import withdrawalTep from './withdrawalTep/withdrawalTep';
import {pickerOptions} from  '@/service/common'
export default {
  name: "withdrawal",
  data() {
    return {
      pickerOptions:pickerOptions,
      searchForm: {
        doctor_name: '',
        doctor_phone: '',
        accountTime: [],
        prop: '',
        order_by: '',
      },
      total: 0, //总条数
      currPage: 1,//当前页码
      pageSize: 10, //每页条数
      tableData:[],
      doctorDec_list:[],
      details:false,
      doctorRow: {},
    }
  },
  components: {
    Pagination,
    withdrawalTep
  },
  created() {
  },
  methods: {
    //
    formatter(column) {
      this.searchForm.prop=column.prop
      if(column.order){
        this.searchForm.order_by=column.order==="descending"?"desc":"asc"
      }else {
        this.searchForm.order_by=''
      }
      this.init()
    },
    // -------------------查询条件/清空-------------------
    search() {
      this.currPage = 1 //当前页码
      this.pageSize = 10 //每页条数
      this.init()
    },
    clear() {
      this.currPage = 1 //当前页码
      this.pageSize = 10 //每页条数
      this.searchForm = {
        doctor_name: '',
        doctor_phone: '',
        accountTime: [],
        prop: '',
        order_by: '',
      }
      this.init()
    },
    derive() { // 导出
      let data = {
        doctor_name: this.searchForm.doctor_name,
        doctor_phone: this.searchForm.doctor_phone,
        date_start: this.searchForm.accountTime[0],
        date_end: this.searchForm.accountTime[1],
        prop: this.searchForm.prop,
        order_by: this.searchForm.order_by,
      }
      this.axios
          .post("platform/Doctorstatistics/exportDoctorIncomeList", data)
          .then((res) => {
            window.open(res.data.url)
          })
    },
    handleClick(doctorRow) { // 详情
      this.doctorRow=doctorRow
      this.details=true
    },
    getBack(e){
      this.details=e
    },
    // ----------分页控制-------------
    handleSizeChange(val) {
      this.pageSize = val
      this.init()
    },
    handleCurrentChange(val) {
      this.currPage = val
      this.init()
    },
    // list
    init() {
      if(this.searchForm.accountTime===null){
        this.searchForm.accountTime=[]
      }
      let data = {
        page: this.currPage,
        pageNumber: this.pageSize,
        doctor_name: this.searchForm.doctor_name,
        doctor_phone: this.searchForm.doctor_phone,
        date_start: this.searchForm.accountTime[0],
        date_end: this.searchForm.accountTime[1],
        prop: this.searchForm.prop,
        order_by: this.searchForm.order_by,
      }
      this.axios
          .post("platform/Doctorstatistics/getDoctorIncomeList", data)
          .then((res) => {
            this.tableData = res.data.list
            this.tableData.forEach(item => {
              item.reception = item.reception === 1 ? "未开启" : "已开启"
            })
            this.total = res.data.total
            this.doctorDec_list = res.data
          })
    }
  },
  mounted() {
    //
    this.init()
  }
}
</script>

